				<h4><span>Morey Equation</span></h4>
				<ul>
					<li>This equation is typically used in beer software to approximate color.</li>
					<li>SRM = 1.4922 * (MCU ^ 0.68590)</li>
					<li>good for color < 50 SRM</li>
					<li>MCU = grainColor * grainWeightLBS/volumeGallons</li>
				</ul>
				
				<h4><span>degress Lovibond</span></h4>
				<ul>
					<li>Was the first scale to officially and uniformily measure the the color of beer in the 1860s.</li>
					<li>A sample of the beer was placed in a &#34;Tintometer&#34; and compared with several glass slides.</li>
					<li>The color disc that most closely represented the beer was than assigned to the beer.</li>
				</ul>
				
				<h4><span>European Brewing Convention</span></h4>
				<p>Another measurement of color for beer.  The scale is approximately double the SRM scale.</p>				
